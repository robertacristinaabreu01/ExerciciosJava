
public class TiposPrimitivos {
	public static void main(String[] args) {
		
		//-----------Texto------------------
		// ***mais usado***
		String variavelString = "";
		//não existe tipo primitivo para String
		// ***mais usado***
		
		
		//-------------INTEIROS-----------------
		Byte variavelByte =1;
		//para tipo primitivo troque Byte para byte
		// para deixar a variavel nula - troque o 1 por null
		//tipo primitivo não aceita null
		//preferencia tipo normal em JAVA
		
		Short variavelShort = 1;
		//troca Short para short
		
		// ***mais usado***
		Integer variavelInteger = 1;
		//troca Integer para int
		// ***mais usado***
		
		Long variavelLong = 1L;
		//troca Long para long
		
		//--------DECIMAIS-------
		
		Float variavelFloat = 1.0f;
		//troca Float para float
		
		
		
		// ***mais usado***
		Double variavelDouble =  1.0;
		//troca Double para double
		// ***mais usado***
		
		
		
		//DIGITO (SOMENTE 1 DE CADA VEZ)
		
		Character variavelCharacter = 'A';
		//troca Chracater para char
		
		
		
		// VERDADEIRO OU FALSO ------ BOOLEAN-------
				
		// ***mais usado***
		Boolean varaiavelBoolean =  true;
		// troca Boolena para boolean
		// ***mais usado***
		
		
	}

}
